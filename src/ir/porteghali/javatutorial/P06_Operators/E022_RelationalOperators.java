package ir.porteghali.javatutorial.P06_Operators;

public class E022_RelationalOperators {

    public void Start() {

        /*
            ==  equal to
            !=  not equal to    (! use for opposite)
            <   greater than
            >   less than
            <=  greater than or equal to
            >=  less than or equal to
         */

        //the response of relational operators is true or false

        int a = 10;
        int b = 15;

        System.out.println(a<b);
        System.out.println(a==b);
        System.out.println(a!=b);
    }
}
