package ir.porteghali.javatutorial.p08_Classes;

public class E033_Animal {
    public String AnimalName = "Animal";

    public void Walk() {
        System.out.println(AnimalName + " is walking");
    }

    public void Breath() {
        System.out.println(AnimalName + " is breathing");
    }

    public void Beat() {
        System.out.println(AnimalName + "'s heart is beating");
    }
}
