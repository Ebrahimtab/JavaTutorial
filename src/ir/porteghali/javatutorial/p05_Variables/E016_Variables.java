package ir.porteghali.javatutorial.p05_Variables;

public class E016_Variables {

    public void Start() {

        //numeric data types
        int a = 10;
        short b = 5;
        long c = 12345;
        float d = 12.34f;
        double e = 123456.789;

        //characters
        char f = 'a';
        String g = "Ebrahim\nTab";

        //boolean
        boolean h = false;

        System.out.println(a+b+c+d+e);
        System.out.println(f);
        System.out.println(g);
        System.out.println(h);
    }

}
