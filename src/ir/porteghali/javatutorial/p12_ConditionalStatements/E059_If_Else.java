package ir.porteghali.javatutorial.p12_ConditionalStatements;

public class E059_If_Else {

    public E059_If_Else() {

        int a = 10;
        int b = 20;
        int c = 12;

        if (!(a < b) && c > 10) {
            System.out.println("inside if");
        } else {
            System.out.println("inside else");
        }
    }
}
